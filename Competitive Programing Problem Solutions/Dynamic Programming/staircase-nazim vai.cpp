ll dp[sz][sz] ;

ll rec(int st , int rem)
{
    if((st+1) == rem)
        return 0 ;

    ll& rat = dp[st][rem] ;

    if(rat != -1)
        return rat ;

    rat = 0 ;

    for(int i=st+1 ; i<(rem+1)/2 ; i++)
    {
        rat = (rat + rec(i,rem-i)+1);
    }

    return rat ;
}

