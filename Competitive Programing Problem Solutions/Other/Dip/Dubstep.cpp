using namespace std;
string ReplaceAll(string s, string from, string to){
    int start_pos = 0;
    while((start_pos = s.find(from, start_pos)) != string::npos){
        s.replace(start_pos, from.length(), to);
        start_pos += to.length();
    }
    return s;
}
